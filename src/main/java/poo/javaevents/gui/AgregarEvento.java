    private void jButtonAgregarFechaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarFechaActionPerformed
        // Agregar una nueva fecha
        String fechaStr = JOptionPane.showInputDialog("Introduce fecha y hora (yyyy-MM-dd HH:mm):");
        try {
            LocalDateTime fecha = LocalDateTime.parse(fechaStr, DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm"));
            fechasEvento.add(fecha);
            jTextAreaFechas.append(fecha.toString() + "\n");
        } catch (DateTimeParseException e) {
            JOptionPane.showMessageDialog(null, "Formato inválido. Usa: yyyy-MM-dd HH:mm", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonAgregarFechaActionPerformed

