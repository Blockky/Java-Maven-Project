    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String titulo = jTextNombre.getText();
            String tipo = jTextCorreo.getText();
            double precio = Double.parseDouble(jTextClave.getText());
            String portada = jTextTelf.getText();

            String calle = jTextCalle.getText();
            int numero = Integer.parseInt(jTextNumero.getText());
            String ciudad = jTextCiudad.getText();
            int cp = Integer.parseInt(jTextCP.getText());
            Direccion dir = new Direccion(calle, numero, ciudad, cp);

            if (fechasEvento.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Debes añadir al menos una fecha para el evento.", "Error", JOptionPane.WARNING_MESSAGE);
                return;
            }
            String estado = utilEventos.agregarEvento(titulo, tipo, dir, fechasEvento, precio, portada);
            if (estado=="YaExiste") {
                JOptionPane.showMessageDialog(this, "No se ha podido crear el evento\n Ya existe " + titulo, 
                        "No se puedo crear", JOptionPane.INFORMATION_MESSAGE);
            } else if (estado=="Creado"){
                JOptionPane.showMessageDialog(this, "Evento creado exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo crear el evento", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al crear el evento: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

